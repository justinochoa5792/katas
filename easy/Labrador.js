class Labrador {
  constructor(n, a, g, m) {
    this.name = n;
    this.age = a;
    this.gender = g;
    this.species = "Labrador";
    this.legs = 4;
    this.size = "Large";
    this.master = m;
    this.loyal = true;
  }
}
